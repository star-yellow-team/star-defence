for(var i=0; i < MAX_Y; i++) { 
		auxMap[i] = [];
		for(var j=0; j < MAX_X; j++) {
			auxMap[i][j] = 0;
		}
	}

for(var i = 0; i < MAX_Y; i++)
	{
		matrix[i] = [];
		for(var j = 0; j < MAX_X; j++)
			{
				matrix[i][j] = [];
				for(var k = 0; k < 5; k++)
					matrix[i][j][k] = 0;
			}
	}

function addMaps()
{
	//adauga o harta in 'vectorul de harit' matrix
	for(var i = 0; i < MAX_Y; i++)
		for(var j = 0; j < MAX_X; j++)
			{
				matrix[i][j][nrMaps] = auxMap[i][j];
			}
	nrMaps++;
}

